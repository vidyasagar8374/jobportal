  function paynow(payment)
  {
      // alert(payment)
    $.ajax({
        type:'POST',
        url:'/purchaseplan',
        data: {
        "_token": "{{ csrf_token() }}",
        type : payment
        },
        success:function(data) {
          if(data == 1)
          {
            $(document).ready(function() {
              swal({
                  title: "Payment Done!",
                  text: "Suceess Payment Done!",
                  icon: "success",
                  button: "Ok",
                  timer: 4000
              });
            });
            setTimeout(function() {
              window.location.href = "{{ url('EmployeeHome')}}";
            }, 4000);
          }
          else{
            $(document).ready(function() {
              swal({
                  title: "Payment Not Done!",
                  text: "Something Went Wrong!",
                  icon: "error",
                  button: "Ok",
                  timer: 4000
              });
            });
          }
        }
    });
  }
  </script>


